#include "library.h"

namespace MathFuncs {
    double MyMathFuncs::Add(double a, double b) {
        return a + b;
    }

    double MyMathFuncs::Subtract(double a, double b) {
        return a - b;
    }

    double MyMathFuncs::Division(double a, double b) {
        return a / b;
    }

    double MyMathFuncs::Multiplication(double a, double b) {
        return a * b;
    }

}
